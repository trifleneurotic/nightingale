	/**
	 * Sin.
	 * 
	 * @param w the w
	 * 
	 * @return the int
	 */
	public static int sin(int w) {
		while (w < 0) w += 360;
		return sin_a[w];
	}
	
	/**
	 * Asin.
	 * 
	 * @param x the x
	 * 
	 * @return the int
	 */
	public static int asin(int x) {
		for (int i = 90; i < 270; i++) {
			if (sin_a[i] < x) {
				return i;
			}
		}
		return -1;
	}

	/**
	 * Acos.
	 * 
	 * @param x the x
	 * 
	 * @return the int
	 */
	public static int acos(int x) {
		for (int i = 0; i < 180; i++) {
			if (cos_a[i] <= x) {
				return i;
			}
		}
		return -1;
	}

	/**
	 * Cos.
	 * 
	 * @param w the w
	 * 
	 * @return the int
	 */
	public static int cos(int w) {
		while (w < 0) w += 360;
		return cos_a[w];
	}
	
	/**
	 * Vector_rotate.
	 * 
	 * @param vector the vector
	 * @param alpha the alpha
	 * 
	 * @return the dimension
	 */
	public static Dimension vector_rotate (Dimension vector, int alpha) {
		int sinus = GameMath.sin(alpha);
		int cosinus = GameMath.cos(alpha);
		return new Dimension((vector.width * cosinus - vector.height * sinus)>>10,
				(vector.width * sinus + vector.height * cosinus)>>10);
	}
